    static class CF {
        class Pair {
            Double height;
            Double time;

            Pair(Double h, Double time) {
                this.height = h;
                this.time = time;
            }
        }

        Double[][] getSkyline(Double[][] buildings) {
//            long[][] buildings = new long[buildingsDouble.length][3];
//            for(int i = 0 ;i < buildingsDouble.length; i ++ ) {
//                buildings[i][0] =  Math.round(buildingsDouble[i][0]*1);
//                buildings[i][1] =  Math.round(buildingsDouble[i][1]*1);
//                buildings[i][2] =  Math.round(buildingsDouble[i][2]*1);
//            }

            ArrayList<Double[]> ans = new ArrayList<>();

            PriorityQueue<Pair> tallestActive = new PriorityQueue<>(10, (h1, h2) -> {
                if (h1.height > h2.height)
                    return -1;
                if (h1.height < h2.height)
                    return 1;
                if (h1.time < h2.time)
                    return -1;
                if (h1.time > h2.time)
                    return 1;
                return 0;
            });

            // add final endpoint
            tallestActive.add(new Pair(0.0, Double.MAX_VALUE));

            for (int i = 0; i < buildings.length; i++) {
                Double[] building = buildings[i];
                Double bbegin = building[0];
                Double bend = bbegin + building[1];
                Double bheight = building[2];


                while (tallestActive.peek().time < bbegin) {
                    Pair tallestBuilding = tallestActive.remove();

                    while (tallestActive.peek().time < tallestBuilding.time) {
                        tallestActive.remove();
                    }
                    if (Math.abs(ans.get(ans.size() - 1)[1] - tallestActive.peek().height) > 0.00001) {
                        ans.add(new Double[]{tallestBuilding.time, tallestActive.peek().height});
                    }
                }

                // new building taller that existing?
                if (bheight > tallestActive.peek().height) {
                    ans.add(new Double[]{bbegin, bheight});
                }
                // regardless if it is taller or not, got to add it
                tallestActive.add(new Pair(bheight, bend));
            }

            while (tallestActive.peek().time < Long.MAX_VALUE - 100) {
                Pair tallestBuilding = tallestActive.remove();
                while (tallestActive.peek().time < tallestBuilding.time) {
                    tallestActive.remove();
                }
                if (Math.abs(ans.get(ans.size() - 1)[1] - tallestActive.peek().height) > 0.00001) {
                    ans.add(new Double[]{tallestBuilding.time, tallestActive.peek().height});
                }
            }

            Double[][] ret = new Double[ans.size()][2];
            for (int i = 0; i < ans.size(); i++) {
                ret[i][0] = (1.0D * ans.get(i)[0]);
                ret[i][1] = (1.0D * ans.get(i)[1]);
            }
            return ret;
        }
    }
